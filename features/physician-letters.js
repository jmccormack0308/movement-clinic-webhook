router.get('/intake', (req, res) => {
  const token = req.query.token;
  if (!token) return res.status(400).send('<h1>Missing token</h1><p>This page must be opened from a valid GHL task link.</p>');

  const draft = loadLetterDraft(token);
  if (!draft) return res.status(404).send('<h1>Letter not found</h1><p>This intake link may have expired.</p>');

  if (draft.status !== 'pending_finalized_note') {
    return res.status(409).send(`<h1>Already submitted</h1><p>This letter is no longer in the intake stage (current status: ${draft.status}). It has either already been submitted or has been cancelled.</p>`);
  }

  // Check for an in-flight letter for the same patient (duplicate guard)
  // — but exclude THIS draft from the check
  let duplicateWarning = null;
  const others = listLetterDrafts().filter(d => d.token !== token);
  const TERMINAL = new Set(['faxed', 'cancelled_patient_lost', 'letter_skipped']);
  const cutoff = Date.now() - DUPLICATE_CHECK_WINDOW_DAYS * 24 * 60 * 60 * 1000;
  const dup = others.find(d => {
    if (TERMINAL.has(d.status)) return false;
    if (new Date(d.created_at).getTime() < cutoff) return false;
    if (draft.ghl_contact_id && d.ghl_contact_id === draft.ghl_contact_id) return true;
    return false;
  });
  if (dup) duplicateWarning = dup;

  res.send(renderAdminIntakePage(draft, duplicateWarning));
});

// NEW v2 ROUTE: Admin submits the finalized eval note
// Regenerates the letter draft using the finalized note as primary source,
// then notifies the PT for review.
router.post('/intake/:token/submit', async (req, res) => {
  try {
    const draft = loadLetterDraft(req.params.token);
    if (!draft) return res.status(404).json({ error: 'Letter not found' });

    if (draft.status !== 'pending_finalized_note') {
      return res.status(409).json({ error: `Letter is not in pending_finalized_note state (current: ${draft.status})` });
    }

    const { finalized_note, submitted_by } = req.body;
    if (!finalized_note || !submitted_by) {
      return res.status(400).json({ error: 'Missing finalized_note or submitted_by' });
    }
    if (finalized_note.length < 100) {
      return res.status(400).json({ error: 'Finalized note seems too short — please paste the full signed note' });
    }

    // Regenerate the letter draft using the finalized note as primary source
    console.log(`[letter-intake] Regenerating draft from finalized note for ${draft.patient_first_name} ${draft.patient_last_name}`);
    const finalContent = await generateLetterDraftWithClaude(
      finalized_note,
      'finalized_note',
      draft.patient_first_name,
      draft.patient_last_name,
      draft.evaluating_pt
    );

    // Update draft state
    draft.clinical_content = finalContent;
    draft.finalized_note_text = finalized_note;
    draft.intake_submitted_at = new Date().toISOString();
    draft.intake_submitted_by = submitted_by;
    draft.status = 'pending_pt_review';
    saveLetterDraft(req.params.token, draft);

    // Notify PT in Slack — top-level message in #physician-letters
    const ptReviewTs = await notifyPTForReview(req.params.token, draft);
    if (ptReviewTs) {
      draft.slack_pt_review_ts = ptReviewTs;
      saveLetterDraft(req.params.token, draft);
    }

    // Mark the GHL task complete
    if (draft.ghl_admin_task_id && draft.ghl_contact_id) {
      await completeAdminIntakeTask(draft.ghl_contact_id, draft.ghl_admin_task_id);
    }

    // Update audit row
    const t1Hours = hoursBetween(draft.created_at, draft.intake_submitted_at);
    await updateLetterAuditRow(req.params.token, {
      'E': 'pending_pt_review',
      'O': `Intake submitted by ${submitted_by} · Final draft confidence: ${finalContent.confidence || 'unknown'}`,
      'P': t1Hours !== null ? t1Hours : ''
    });

    res.json({ success: true });
  } catch (err) {
    console.error('[letter intake submit] error:', err.message);
    res.status(500).json({ error: err.message });
  }
});

// NEW v2 ROUTE: GHL cancellation webhook
// Receives a webhook from a GHL workflow when an opportunity moves to Closed/Lost.
// Cancels any in-flight letter for that contact (does nothing if already faxed).
//
// Expected payload (from GHL workflow):
//   { contact_id: "...", opportunity_id: "...", reason?: "..." }
router.post('/cancel', async (req, res) => {
  try {
    const { contact_id, opportunity_id, reason } = req.body || {};
    if (!contact_id) return res.status(400).json({ error: 'contact_id required' });

    const drafts = listLetterDrafts();
    const TERMINAL = new Set(['faxed', 'cancelled_patient_lost', 'letter_skipped']);
    const candidates = drafts.filter(d =>
      d.ghl_contact_id === contact_id && !TERMINAL.has(d.status)
    );

    if (candidates.length === 0) {
      console.log(`[letter-cancel] No in-flight letters for contact ${contact_id}`);
      return res.json({ success: true, cancelled: 0, message: 'No in-flight letters for this contact' });
    }

    let cancelledCount = 0;
    for (const draft of candidates) {
      draft.status = 'cancelled_patient_lost';
      draft.cancelled_at = new Date().toISOString();
      draft.cancellation_reason = reason || 'Patient opportunity moved to Closed/Lost';
      saveLetterDraft(draft.token, draft);

      await updateLetterAuditRow(draft.token, {
        'E': 'cancelled_patient_lost',
        'O': `Cancelled — ${draft.cancellation_reason}`
      });

      // Complete the GHL admin task if still open
      if (draft.ghl_admin_task_id && draft.ghl_contact_id) {
        await completeAdminIntakeTask(draft.ghl_contact_id, draft.ghl_admin_task_id);
      }

      // Slack notice
      await notifyLetterCancelled(draft, draft.cancellation_reason);
      cancelledCount++;
    }

    console.log(`[letter-cancel] Cancelled ${cancelledCount} letter(s) for contact ${contact_id}`);
    res.json({ success: true, cancelled: cancelledCount });
  } catch (err) {
    console.error('[letter cancel] error:', err.message);
    res.status(500).json({ error: err.message });
  }
});

