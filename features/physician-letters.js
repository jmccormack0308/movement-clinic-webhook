function getServiceAccountAuth(scopes) {
  const SERVICE_ACCOUNT_JSON = JSON.parse(process.env.GOOGLE_SERVICE_ACCOUNT_JSON || '{}');
  return new google.auth.GoogleAuth({ credentials: SERVICE_ACCOUNT_JSON, scopes });
}

async function uploadLetterPDFToDrive(pdfBuffer, filename) {
  const auth = getServiceAccountAuth(['https://www.googleapis.com/auth/drive']);
  const drive = google.drive({ version: 'v3', auth });

  const stream = new Readable();
  stream.push(pdfBuffer);
  stream.push(null);

  const file = await drive.files.create({
    requestBody: {
      name: filename,
      parents: [PHYSICIAN_LETTERS_DRIVE_FOLDER],
      mimeType: 'application/pdf'
    },
    media: { mimeType: 'application/pdf', body: stream },
    fields: 'id, webViewLink',
    supportsAllDrives: true
  });

  return { id: file.data.id, webViewLink: file.data.webViewLink };
}

async function appendLetterAuditRow(rowValues) {
  const auth = getServiceAccountAuth(['https://www.googleapis.com/auth/spreadsheets']);
  const sheets = google.sheets({ version: 'v4', auth });

  await sheets.spreadsheets.values.append({
    spreadsheetId: PHYSICIAN_LETTERS_SHEET_ID,
    range: `${PHYSICIAN_LETTERS_SHEET_TAB}!A:O`,
    valueInputOption: 'USER_ENTERED',
    insertDataOption: 'INSERT_ROWS',
    requestBody: { values: [rowValues] }
  });
}

async function updateLetterAuditRow(letterId, columnUpdates) {
  // columnUpdates: { K: 'value', L: 'value' } — column-letter -> value
  const auth = getServiceAccountAuth(['https://www.googleapis.com/auth/spreadsheets']);
  const sheets = google.sheets({ version: 'v4', auth });

  // Find the row with this letter ID (column B)
  const lookup = await sheets.spreadsheets.values.get({
    spreadsheetId: PHYSICIAN_LETTERS_SHEET_ID,
    range: `${PHYSICIAN_LETTERS_SHEET_TAB}!B:B`
  });
  const rows = lookup.data.values || [];
  let rowIndex = -1;
  for (let i = 0; i < rows.length; i++) {
    if (rows[i][0] === letterId) { rowIndex = i + 1; break; }
  }
  if (rowIndex === -1) {
    console.warn(`[letter-audit] Letter ID ${letterId} not found in sheet — skipping update`);
    return;
  }

  const data = Object.entries(columnUpdates).map(([col, val]) => ({
    range: `${PHYSICIAN_LETTERS_SHEET_TAB}!${col}${rowIndex}`,
    values: [[val]]
  }));

  await sheets.spreadsheets.values.batchUpdate({
    spreadsheetId: PHYSICIAN_LETTERS_SHEET_ID,
    requestBody: { valueInputOption: 'USER_ENTERED', data }
  });
}

