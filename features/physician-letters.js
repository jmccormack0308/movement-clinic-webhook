// Generate a draft using either the eval transcript (preliminary) or the finalized
// PTE note (final). The prompt is identical — only the source material differs.
async function generateLetterDraftWithClaude(sourceMaterial, sourceType, patientFirstName, patientLastName, evaluatingPT) {
  const sourceLabel = sourceType === 'finalized_note' ? 'FINALIZED EVALUATION NOTE' : 'EVAL TRANSCRIPT';
  const userMessage = `PATIENT NAME: ${patientFirstName} ${patientLastName}
EVALUATING PHYSICAL THERAPIST: ${evaluatingPT}

${sourceLabel}:
${sourceMaterial}`;

  const response = await axios.post(
    'https://api.anthropic.com/v1/messages',
    {
      model: 'claude-sonnet-4-6',
      max_tokens: 3000,
      system: [{ type: 'text', text: LETTER_GENERATION_PROMPT }],
      messages: [{ role: 'user', content: userMessage }]
    },
    {
      headers: {
        'x-api-key': ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01',
        'Content-Type': 'application/json'
      },
      timeout: 90000
    }
  );

  const text = response.data.content[0].text;
  return JSON.parse(text.replace(/```json|```/g, '').trim());
}

