// Posts a Slack message and returns its `ts` (timestamp ID, used to react/reply later).
// Pass `thread_ts` to post as a threaded reply under an existing message.
async function postSlack(channel, text, blocks, thread_ts) {
  const payload = { channel, text, blocks };
  if (thread_ts) payload.thread_ts = thread_ts;
  const response = await axios.post(
    'https://slack.com/api/chat.postMessage',
    payload,
    { headers: { Authorization: `Bearer ${SLACK_BOT_TOKEN}` } }
  );
  if (!response.data.ok) {
    console.warn(`[letter-notify] Slack post failed: ${response.data.error}`);
    return null;
  }
  return response.data.ts;
}

// Add a ✅ (or other) emoji reaction to a Slack message.
// Silent failure if message_ts is missing or Slack rejects (e.g., already reacted).
async function addReaction(channel, message_ts, emoji = 'white_check_mark') {
  if (!channel || !message_ts || !SLACK_BOT_TOKEN) return;
  try {
    const response = await axios.post(
      'https://slack.com/api/reactions.add',
      { channel, timestamp: message_ts, name: emoji },
      { headers: { Authorization: `Bearer ${SLACK_BOT_TOKEN}` } }
    );
    if (!response.data.ok && response.data.error !== 'already_reacted') {
      console.warn(`[letter-notify] Reaction add failed: ${response.data.error}`);
    }
  } catch (err) {
    console.warn(`[letter-notify] Reaction add error: ${err.message}`);
  }
}

