router.post('/:token/generate-pdf', async (req, res) => {
  try {
    const draft = loadLetterDraft(req.params.token);
    if (!draft) return res.status(404).json({ error: 'Letter not found' });
    if (draft.status !== 'pending_admin_routing') {
      return res.status(409).json({ error: 'Letter is not in pending_admin_routing state (current: ' + draft.status + ')' });
    }

    const { physician_name, physician_fax, physician_practice, admin_name } = req.body;
    if (!physician_name || !physician_fax || !admin_name) {
      return res.status(400).json({ error: 'Missing required fields' });
    }

    draft.physician_routing = {
      physician_name,
      physician_fax,
      physician_practice: physician_practice || ''
    };
    draft.admin_routed_by = admin_name;
    draft.admin_routed_at = new Date().toISOString();
    draft.status = 'pdf_ready';

    const pdfBuffer = await renderLetterPDF(draft);

    const dateStr = new Date().toISOString().split('T')[0];
    const cleanLast = (draft.patient_last_name || 'Patient').replace(/[^a-zA-Z0-9]/g, '');
    const cleanFirst = (draft.patient_first_name || '').replace(/[^a-zA-Z0-9]/g, '');
    const filename = `${cleanLast}_${cleanFirst}_${dateStr}.pdf`;

    const driveResult = await uploadLetterPDFToDrive(pdfBuffer, filename);
    draft.pdf_drive_id = driveResult.id;
    draft.pdf_drive_link = driveResult.webViewLink;
    saveLetterDraft(req.params.token, draft);

    await updateLetterAuditRow(req.params.token, {
      'E': 'pdf_ready',
      'G': physician_name,
      'H': physician_practice || '',
      'I': physician_fax,
      'J': admin_name,
      'K': draft.admin_routed_at,
      'N': driveResult.webViewLink
    });

    // ✅ react to the admin routing message (visual signal that PDF was generated)
    await addReaction(PHYSICIAN_LETTERS_CHANNEL, draft.slack_admin_routing_ts);

    // Post the PDF-ready message as a threaded reply, save its ts
    const pdfReadyTs = await notifyAdminPDFReady(req.params.token, draft, driveResult.webViewLink);
    if (pdfReadyTs) {
      draft.slack_pdf_ready_ts = pdfReadyTs;
      saveLetterDraft(req.params.token, draft);
    }

    res.json({ success: true, drive_link: driveResult.webViewLink });
  } catch (err) {
    console.error('[letter generate-pdf] error:', err.message);
    res.status(500).json({ error: err.message });
  }
});

