// ─── STORAGE HELPERS ─────────────────────────────────────────────────────────

function generateLetterToken() {
  return crypto.randomBytes(16).toString('hex');
}

function saveLetterDraft(token, data) {
  const filePath = path.join(LETTER_DRAFTS_DIR, `${token}.json`);
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
}

function loadLetterDraft(token) {
  const filePath = path.join(LETTER_DRAFTS_DIR, `${token}.json`);
  if (!fs.existsSync(filePath)) return null;
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function listLetterDrafts() {
  if (!fs.existsSync(LETTER_DRAFTS_DIR)) return [];
  return fs.readdirSync(LETTER_DRAFTS_DIR)
    .filter(f => f.endsWith('.json'))
    .map(f => {
      try { return loadLetterDraft(f.replace('.json', '')); }
      catch { return null; }
    })
    .filter(Boolean);
}

function patientInitials(draft) {
  const f = (draft.patient_first_name || '?')[0];
  const l = (draft.patient_last_name || '?')[0];
  return `${f}.${l}.`;
}

// Find an in-flight (non-terminal) letter for a given patient name + contact_id,
// created within the duplicate-check window. Used by the intake form to warn
// admin about possible duplicate submissions.
//
// Terminal statuses (excluded): faxed, cancelled_patient_lost, letter_skipped
// In-flight statuses (included): pending_finalized_note, pending_pt_review,
//                                pending_admin_routing, pdf_ready
function findInFlightLetterForPatient(contactId, firstName, lastName) {
  const drafts = listLetterDrafts();
  const cutoff = Date.now() - DUPLICATE_CHECK_WINDOW_DAYS * 24 * 60 * 60 * 1000;
  const TERMINAL = new Set(['faxed', 'cancelled_patient_lost', 'letter_skipped']);

  return drafts.find(d => {
    if (TERMINAL.has(d.status)) return false;
    if (new Date(d.created_at).getTime() < cutoff) return false;
    // Match on contact_id if available, otherwise fall back to name match
    if (contactId && d.ghl_contact_id === contactId) return true;
    if (!contactId && firstName && lastName) {
      return (d.patient_first_name || '').toLowerCase() === firstName.toLowerCase()
          && (d.patient_last_name || '').toLowerCase() === lastName.toLowerCase();
    }
    return false;
  });
}

