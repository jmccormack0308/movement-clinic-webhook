// features/physician-letters.js
// ═════════════════════════════════════════════════════════════════════════════
// PHYSICIAN LETTERS FEATURE — Self-contained module (v2 — two-stage drafting)
// ═════════════════════════════════════════════════════════════════════════════
//
// WORKFLOW (post-eval form fires Converted + toggle ON):
//   1. Post-eval handler calls `notifyPhysicianLetterPending()`.
//   2. Claude generates a PRELIMINARY draft from the eval transcript.
//      - Held silently in audit sheet with status `pending_finalized_note`.
//      - PT is NOT notified yet. Acts as a safety net if finalized note never comes.
//   3. GHL task created for admin (assigned to info@movementclinicpt.com):
//      "Submit finalized eval note for [Patient]" — links to /eval-letter/intake?token=...
//   4. Admin verifies the eval note is signed in PTEverywhere, opens the intake form
//      (pre-filled with patient + PT), copies the signed note as-is, submits.
//   5. Intake form regenerates the FINAL draft using the finalized note as primary source.
//      - Top-level Slack post in #physician-letters @-mentions the PT for review.
//      - Audit row updated to `pending_pt_review`.
//   6. PT reviews, edits any AI drift, attests, approves.
//      - ✅ reaction on PT review message + threaded reply for admin routing.
//   7. Admin enters MD info, generates PDF.
//      - ✅ reaction on routing message + threaded "PDF Ready" reply.
//   8. Admin faxes externally, marks faxed.
//      - ✅ reaction on PDF Ready message + audit row updated.
//
// SKIP PATH (toggle OFF — self-referred patients):
//   - Single non-actionable Slack post: "No physician letter needed — J.M."
//   - Audit row with status 'letter_skipped'.
//   - No Claude call, no admin task, no PT notification.
//
// CANCELLATION PATH (patient lost before fax):
//   - GHL workflow on Closed/Lost stage fires webhook to /eval-letter/cancel
//   - In-flight letter for that contact_id auto-cancelled
//   - Audit row → `cancelled_patient_lost`
//   - Slack notice posted to #physician-letters
//   - GHL admin task auto-completed (if still open)
//   - If letter was already faxed, no automated retraction — logged for human review.
//
// AUDIT TIMING METRICS (surfaced in Friday weekly email):
//   - T1: conversion → finalized note submitted (PT documentation timeliness)
//   - T2: intake submission → PT approval (PT review responsiveness)
//   - T3: PT approval → fax sent (admin fax turnaround)
//
// PUBLIC EXPORTS:
//   module.exports                                  — Express router (mount at /eval-letter)
//   module.exports.init                             — call once on startup (arms weekly cron)
//   module.exports.notifyPhysicianLetterPending     — called by post-eval handler when toggle ON (NEW v2 entry point)
//   module.exports.notifyPhysicianLetterSkipped     — called by post-eval handler when toggle OFF
//   module.exports.generateAndQueuePhysicianLetter  — DEPRECATED v1 entry point — still exported for safety, now aliases notifyPhysicianLetterPending
//
// All env vars are read at the top of this file. All clinical content is
// generated by Claude Sonnet 4.6.
// ═════════════════════════════════════════════════════════════════════════════

