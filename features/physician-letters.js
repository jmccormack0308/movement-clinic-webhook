router.post('/:token/approve', async (req, res) => {
  try {
    const draft = loadLetterDraft(req.params.token);
    if (!draft) return res.status(404).json({ error: 'Letter not found' });
    if (draft.status !== 'pending_pt_review') {
      return res.status(409).json({ error: 'Letter has already been approved' });
    }

    const { clinical_content } = req.body;
    if (!clinical_content) return res.status(400).json({ error: 'Missing clinical_content' });

    draft.clinical_content = clinical_content;
    draft.status = 'pending_admin_routing';
    draft.pt_approved_at = new Date().toISOString();
    draft.pt_approved_at_human = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
    saveLetterDraft(req.params.token, draft);

    await updateLetterAuditRow(req.params.token, {
      'E': 'pending_admin_routing',
      'F': draft.pt_approved_at
    });

    // ✅ react to the top-level PT review message (visual signal of approval)
    await addReaction(PHYSICIAN_LETTERS_CHANNEL, draft.slack_pt_review_ts);

    // Post the admin routing message as a threaded reply, save its ts
    const adminTs = await notifyAdminForRouting(req.params.token, draft);
    if (adminTs) {
      draft.slack_admin_routing_ts = adminTs;
      saveLetterDraft(req.params.token, draft);
    }

    res.json({ success: true });
  } catch (err) {
    console.error('[letter approve] error:', err.message);
    res.status(500).json({ error: err.message });
  }
});

