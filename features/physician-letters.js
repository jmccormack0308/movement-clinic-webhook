function init() {
  // Schedule weekly Friday 5pm PT audit email
  setInterval(() => {
    const now = new Date();
    const ptOffset = -8;  // close enough for a weekly trigger window
    const ptHour = (now.getUTCHours() + ptOffset + 24) % 24;
    const ptDay = now.getUTCDay();  // Friday = 5
    const ptMinute = now.getUTCMinutes();
    if (ptDay === 5 && ptHour === 17 && ptMinute < 5) {
      sendWeeklyLetterAuditEmail();
    }
  }, 5 * 60 * 1000);  // check every 5 minutes
  console.log('[letter-audit] Weekly audit scheduler armed (Fridays 5pm PT)');
}

